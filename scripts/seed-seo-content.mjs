// ─────────────────────────────────────────────
// SEED SERVICES
// ─────────────────────────────────────────────
async function seedServices() {
  console.log("🔧 Updating services with professional content & images...\n");

  const services = await client.fetch(`*[_type == "service"]{_id, title}`);

  for (const update of serviceUpdates) {
    const match = services.find((s) => s.title?.includes(update.titleMatch));

    if (match) {
      const heroImage = await uploadImage(images[update.imageKey], `service-${update.slug}.jpg`);

      const patchData = {
        slug: { _type: "slug", current: update.slug },
        seoTitle: update.seoTitle,
        seoDescription: update.seoDescription,
        longDescription: update.longDescription,
        order: update.order,
      };

      if (heroImage) {
        patchData.heroImage = heroImage;
      }

      await client.patch(match._id).set(patchData).commit();
      console.log(`  ✅ Updated "${match.title}" → ${update.slug}\n`);
    } else {
      console.log(`  ⚠️  No service found matching "${update.titleMatch}"\n`);
    }
  }
}

// ─────────────────────────────────────────────
// SEED BLOG POSTS (delete old + create new)
// ─────────────────────────────────────────────
async function seedBlogPosts() {
  console.log("\n📝 Replacing blog posts with professional content...\n");

  // Clean up ALL old blog posts from previous seeds
  const allOldSlugs = [
    "5-reasons-mobile-app-essential-2026",
    "salla-vs-zid-complete-guide",
    "brand-identity-importance-more-than-logo",
    "how-to-choose-software-company-saudi",
    "digital-transformation-vision-2030-saudi",
  ];

  for (const oldSlug of allOldSlugs) {
    const old = await client.fetch(
      `*[_type == "blogPost" && slug.current == $slug][0]{_id}`,
      { slug: oldSlug }
    );
    if (old) {
      await client.delete(old._id);
      console.log(`  🗑️  Removed old: ${oldSlug}`);
    }
  }

  // Create new posts
  for (const post of blogPosts) {
    // Delete if exists (from a previous run of this script)
    const existing = await client.fetch(
      `*[_type == "blogPost" && slug.current == $slug][0]{_id}`,
      { slug: post.slug }
    );
    if (existing) {
      await client.delete(existing._id);
      console.log(`  🗑️  Replaced existing: ${post.slug}`);
    }

    // Upload cover image
    const coverImage = await uploadImage(images[post.imageKey], `blog-${post.slug}.jpg`);

    const doc = {
      _type: "blogPost",
      title: post.title,
      slug: { _type: "slug", current: post.slug },
      excerpt: post.excerpt,
      content: post.content,
      category: post.category,
      tags: post.tags,
      author: post.author,
      publishedAt: post.publishedAt,
      seoTitle: post.seoTitle,
      seoDescription: post.seoDescription,
      featured: post.featured,
    };

    if (coverImage) {
      doc.coverImage = { ...coverImage, alt: post.title };
    }

    await client.create(doc);
    console.log(`  ✅ Created: "${post.title}"\n`);
  }
}

